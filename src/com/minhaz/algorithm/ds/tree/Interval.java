package com.minhaz.algorithm.ds.tree;

public class Interval {
	private int low;
	private int high;

	/**
	 * @return the low
	 */
	public int getLow() {
		return low;
	}
	/**
	 * @param low
	 *            the low to set
	 */
	public void setLow(int low) {
		this.low = low;
	}
	/**
	 * @return the high
	 */
	public int getHigh() {
		return high;
	}
	/**
	 * @param high
	 *            the high to set
	 */
	public void setHigh(int high) {
		this.high = high;
	}

}
